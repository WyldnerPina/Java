// Mostre todas as possibilidades de 2 dados de forma que a soma tenha como resultado 7

import javax.swing.*;

public class ExercLP03_41 {
public static void main (String args [ ]) {
    
    int a = 6;
    
    for (int i=1; i<7; i++){
        System.out.println(i + " + " + a + " = 7");
        a--;                           
    }
 }
}